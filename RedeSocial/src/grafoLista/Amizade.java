package grafoLista;


public class Amizade {

    private Pessoa origem;
    private Pessoa destino;

    public Amizade(Pessoa origem, Pessoa destino){
        this.origem = origem;
        this.destino = destino;
    }

    public Pessoa getOrigem() {
        return origem;
    }
    
    public Pessoa getDestino() {
        return destino;
    }
}

    

