package grafoLista;

public class No {

    private int n;
    private String nome;

    public No(int n, String nome) {
        this.n = n;
        this.nome = nome;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
